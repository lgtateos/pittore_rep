void rgb_to_hsv( float r, float g, float b, float& h, float& s, float& v){
  // Routine base on pseudocode in Foley Van Damm page 592
  // RGB values are in [0,1].  S and V are in [0,1] and H is in [0,6], unless S is 0, then H is returned -1
   int i; float min_val; float max_val; float diff; float factor;
   // Find min of r,g,b
   min_val = min(r,g);
   min_val = min(min_val,b);

   // Find max of r,g,b
   max_val = max(r,g);
   max_val = max( max_val, b);
   if( r == g && g == b){
     h = -1;
     s = 0;
     v = max_val;  
     return;
   }
   float delta = max_val - min_val;

   if ( r == max_val){
     h = (g - b)/delta;
   }else if ( g == max_val ){
     h = 2 + ( b - r )/delta;
   }else {
     h = 4 + ( r - g )/delta;
   }

   s = (max_val-min_val)/max_val;
   v = max_val;
   return;
}

void hsv_to_rgb( float& r, float& g, float& b, float h, float s, float v ){
  // Routine base on pseudocode in Foley Van Damm page 593
  // H is in [0,6] or -1.  S and V are in [0,1].
  // R G B are returned on [0,1].
  int i; float m; float en; float f; float t;
  if ( h == -1 ){
    r = v;
    g = v;
    b = v;
    return;
  }

  i = floor( h );
  f = h - i;

  m = v*(1-s);
  en = v*(1-(s*f));
  t = v*( 1 - (s*(1.0 - f)));
  switch( i ){
    case 6:
    case 0:
      r = v;
      g = t;
      b = m;
      break;
    case 1:
      r = en;
      g = v;
      b = m;
      break;
    case 2:
      r = m;
      g = v;
      b = t;
      break;
    case 3:
      r = m;
      g = en;
      b = v;
      break;
    case 4:
      r = t;
      g = m;
      b = v;
      break;
    case 5:
      r = v;
      g = m;
      b = en;
      break;
  }
  return;
}